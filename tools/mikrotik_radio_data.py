def read_tlvs(fp):
    while True:
        data = fp.read(4)
        if data == b'':
            break

        t, l = struct.unpack('<HH', data)
        v = fp.read(l)
        yield t, v

fp = open(sys.argv[1], 'rb')
assert fp.read(4) == b'Hard'

tlvs = dict(read_tlvs(fp))
assert 0x16 in tlvs

src = LZO_PREFIX + tlvs[0x16][4:]

dst = ctypes.create_string_buffer(0x10000)
dst_len = ctypes.c_uint(len(dst))

lzo = ctypes.CDLL('liblzo2.so')
lzo.lzo1x_decompress_safe(src, len(src), dst, ctypes.byref(dst_len), 0)

print(dst_len)
open('radio.bin', 'wb').write(dst[:dst_len.value])
